// cmath's abs example
#include <iostream>     // std::cout
#include <cmath>        // std::abs
using namespace std;

int main ()
{
    int n;
    cin >> n;
  std::cout << "abs (3.1416) = " << std::abs (3.1416) << '\n';
  std::cout << "abs (-10.6)  = " << std::abs (n) << '\n';
  return 0;
}
